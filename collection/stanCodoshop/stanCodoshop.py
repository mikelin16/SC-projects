def get_pixel_dist(pixel, red, green, blue):
    """
    Returns the color distance between pixel and mean RGB value

    Input:
        pixel (Pixel): pixel with RGB values to be compared
        red (int): average red value across all images
        green (int): average green value across all images
        blue (int): average blue value across all images

    Returns:
        dist (int): color distance between red, green, and blue pixel values

    """
    dist = math.sqrt((red-pixel.red)**2 + (green-pixel.green)**2 + (blue-pixel.blue)**2)
    return dist


def get_average(pixels):
    """
    Given a list of pixels, finds the average red, blue, and green values

    Input:
        pixels (List[Pixel]): list of pixels to be averaged
    Returns:
        rgb (List[int]): list of average red, green, blue values across pixels respectively

    Assumes you are returning in the order: [red, green, blue]

    """
    r = 0
    g = 0
    b = 0
    for pixel in pixels:
        r += pixel.red
        g += pixel.green
        b += pixel.blue
    avg_r = r // len(pixels)
    avg_g = g // len(pixels)
    avg_b = b // len(pixels)
    rgb = [avg_r, avg_g, avg_b]
    return rgb


def get_best_pixel(pixels):
    """
    Given a list of pixels, returns the pixel with the smallest
    distance from the average red, green, and blue values across all pixels.

    Input:
        pixels (List[Pixel]): list of pixels to be averaged and compared
    Returns:
        best (Pixel): pixel closest to RGB averages

    """

    best = pixels[0]
    x = get_average(pixels)
    avg_r = x[0]
    avg_g = x[1]
    avg_b = x[2]
    best_dist = get_pixel_dist(pixels[0], avg_r, avg_g, avg_b)
    for pixel in pixels:
        compare_dist = get_pixel_dist(pixel, avg_r, avg_g, avg_b)
        if best_dist >= compare_dist:
            best_dist = compare_dist
            best = pixel
    return best


